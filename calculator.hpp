#ifndef CALCULATOR
#define CALCULATOR

class calculator
{
public:
    /**
 * @brief sum
 * @param x
 * @param y
 * add two double numbers
 */
void sum (const double x,const double y);
/**
 * @brief sub
 * @param x
 * @param y
 * substract two double numbers
 */
void sub (const double x,const double y);
/**
 * @brief mul
 * @param x
 * @param y
 * multiply two double numbers
 */
void mul (const double x,const double y);
/**
 * @brief div
 * @param x
 * @param y
 * divide two double numbers
 */
void div (const double x,const double y);
/**
 * @brief mod
 * @param x
 * @param y
 * calculate the modulo of two numbers
 */
void mod (const int x,const int y);
};

#endif
	
